public class MyProgram {
    public static void main(String[] args) {
        // Define 5 double values
        double value1 = 12.5;
        double value2 = 18.3;
        double value3 = 14.7;
        double value4 = 22.1;
        double value5 = 16.4;
        
        // Calculate the average
        double average = (value1 + value2 + value3 + value4 + value5) / 5.0;
        
        // Display the values and average
        System.out.println("Values: " + value1 + ", " + value2 + ", " + value3 + ", " + value4 + ", " + value5);
        System.out.println("Average: " + average);
    }
}
